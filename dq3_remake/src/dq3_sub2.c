#include "dq3_sub2.h"

const unsigned char dq3_sub2_rec[40] = {
  0x12, 0xff, 0x19, 0xff, 0xff, 0x2f, 0x31, 0x41, 0x3d, 0x2e,
  0xff, 0xff, 0x36, 0x44, 0xff, 0xff, 0x2f, 0xff, 0xff, 0x4d,
  0x54, 0x56, 0x56, 0x56, 0x64, 0x77, 0x1c, 0x1c, 0xff, 0xff,
  0xff, 0x2d, 0x42, 0x47, 0x56, 0x27, 0x42, 0x54, 0x20, 0xff,
};
const int dq3_sub2_rec_len = 40;

int dq3_sub2_dialogue(int byte4)
{
    if (byte4 < 0 || byte4 >= dq3_sub2_rec_len) return -1;
    return dq3_sub2_rec[byte4] == 0xff ? -1 : dq3_sub2_rec[byte4];
}
